import math

# 向上取整
math.ceil(2.4)
# 向下取整
math.floor(2.4)

# 2的3次幂
math.pow(2, 3)
# 平方根
math.sqrt(3.6)

# 以5为底，对125求对数
math.log(125, 5)
# 以自然对数e为底，对125求对数
math.log(125)

# 弧度转为角度
math.degrees(0.5 * math.pi)     # 90.0
# 将角度转为弧度
math.radians(180 / math.pi)     # 1.0

math.sin(math.pi / 2)           # 1.0